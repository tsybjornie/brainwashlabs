# ğŸ§  Startup Logs
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@app.on_event("startup")
async def startup_event():
    missing_envs = [
        key for key in ["STRIPE_SECRET_KEY", "COINBASE_API_KEY", "OPENAI_API_KEY"]
        if not os.getenv(key)
    ]
    if missing_envs:
        logger.warning(f"âš ï¸ Missing ENV variables: {', '.join(missing_envs)}")
    else:
        logger.info("âœ… All critical ENV variables found")

    logger.info("ğŸš€ Brainwash Labs Backend initialized successfully!")
